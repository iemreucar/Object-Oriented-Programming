public class Student
{
   public static void main(String[] args)
   {
      // (1)	print out the string "Name, Surname: " 
      //      without ending the line.
      System.out.print("Name, Surname: ");
      
      // (2)	print out your name (e.g. “Tacettin”) and 
      //      your surname (e.g., “Ayar”), then end the line.
      System.out.println("Tacettin Ayar");
      
      // (3)	print out the string "Student ID: " 
      //      without ending the line.
      System.out.print("Student ID: ");
      
      // (4)	print out first 8 digits of your student ID as 
      //      a string (e.g. “70123109”) without ending the line.
      System.out.print("70123109"); 
      
      // (5)	print out last digit of your student ID
      //      as an integer (e.g. 9) and end the line
      System.out.println(9); // Print out last digit      
   }
}
